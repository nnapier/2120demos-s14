public class WhileExample2
{
	
	public static void main(String[] args)
	{
		int x = 1;
		while (x <= 5 )
		{
			System.out.println( x + " square is " + (x*x));
			x++;
		}
		
		System.out.println("Value of x is now " + x);

	}

}
